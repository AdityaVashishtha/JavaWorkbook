package unitconverter;

/**
 *
 * @author hp
 */
public class UnitConverter {

    /**
     * Take CM value and convert to meter input int
     */
    public static int KMToMeter(int km) {
        return km*1000;
    }    
    
    /**
     * Take CM value and convert to meter input int
     */
    public static int CMToMeter(int cm) {
        return cm*1000;
    }    
}
